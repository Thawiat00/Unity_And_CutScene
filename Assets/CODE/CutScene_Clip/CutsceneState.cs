using UnityEngine;
using UnityEngine.UI;
using UnityEngine.Video;

// ??????????????? Cutscene
public class CutsceneState : IDialogState
{
    private DialogManager manager;
    private string clipName;
    private VideoPlayer videoPlayer;
    private RawImage rawImage;

    public CutsceneState(DialogManager manager, string clipName)
    {
        this.manager = manager;
        this.clipName = clipName;
        this.videoPlayer = manager.GetComponent<VideoPlayer>();
        this.rawImage = manager.GetRawImage();
    }

    public void Enter()
    {
        Debug.Log("Entering CutsceneState: " + clipName);
        PlayVideo(); // ???????????????
    }

    public void Update()
    {
        // ?????????????????????????????
        if (!videoPlayer.isPlaying)
        {
            Debug.Log("Video finished playing. Moving to next state.");
            // comment out the line below to keep the video looping
            // manager.NextState(); 
        }

        // ?????????????????? spacebar ?????????????????
        if (Input.GetKeyDown(KeyCode.Space))
        {
            Debug.Log("Spacebar pressed. Moving to next state.");
            manager.NextState(); // ???????????????????????????????????????????????
        }
    }

    public void Exit()
    {
        Debug.Log("Exiting CutsceneState.");
        videoPlayer.Stop(); // ??????????????
        rawImage.gameObject.SetActive(false); // ???? RawImage
    }

    private void PlayVideo()
    {
        VideoClip clip = Resources.Load<VideoClip>("Videos/" + clipName);
        if (clip != null)
        {
            videoPlayer.clip = clip;
            videoPlayer.renderMode = VideoRenderMode.RenderTexture;

            // ???? RawImage ????????????????????????
            rawImage.gameObject.SetActive(true);
            Debug.Log("RawImage set to active.");

            // ??????? RenderTexture ?????????????????????????????
            RenderTexture rt = new RenderTexture(1280, 720, 24); // ????????? RenderTexture
            videoPlayer.targetTexture = rt;

            rawImage.texture = rt; // ????? Texture ??? RawImage

            videoPlayer.isLooping = true; // ??????????????????????
            videoPlayer.Play(); // ???????????????
            Debug.Log("Playing video: " + clipName);
        }
        else
        {
            Debug.LogError("Video clip not found: " + clipName);
            manager.NextState(); // ???????????????
        }
    }
}
